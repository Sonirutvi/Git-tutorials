// String Extensions
// String startsWith() 
// String endsWith() 
// String includes()

let firstname = "Rutvi"
let lastname = "Soni"

const fname = `${firstname}`;
const fullname  = `${firstname} ${lastname} `;

console.log(fname .startsWith('R'));
console.log(fname.endsWith('i'));
console.log(fname.includes('Rutvi'));
console.log(fullname.repeat(2));